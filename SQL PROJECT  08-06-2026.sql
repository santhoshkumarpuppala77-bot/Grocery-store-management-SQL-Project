SELECT 
    e.emp_name, ROUND(AVG(od.total_price), 2)
FROM
    employees e
        JOIN
    orders o ON e.emp_id = o.emp_id
        JOIN
    order_details od ON o.ord_id = od.ord_id
GROUP BY e.emp_name;


-- 6️. Order Details Deep Dive
-- What is the relationship between quantity ordered and total price?
SELECT 
    od.ord_detID,
    p.prod_name,
    od.quantity,
    od.each_price,
    od.total_price
FROM order_details od
JOIN products p
    ON od.prod_id = p.prod_id;

-- What is the average quantity ordered per product?
SELECT 
    p.prod_name, AVG(od.quantity) AS avg_quantity
FROM
    order_details od
        JOIN
    products p ON od.prod_id = p.prod_id
GROUP BY prod_name;

-- How does the unit price vary across products and orders?
SELECT
    ord_detID,
    quantity,
    each_price,
    total_price,
    (quantity * each_price) AS calculated_total
FROM order_details;


