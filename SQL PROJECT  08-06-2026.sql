-- Analysis Qustions
--- 1. Customer Insights
-- How many unique customers have placed orders?
SELECT 
    *
FROM
    customers;
 
-- Which customers have placed the highest number of orders?

SELECT 
    c.cust_name, COUNT(o.cust_id) AS o_c
FROM
    customers c
        JOIN
    orders o ON c.cust_id = o.cust_id
GROUP BY c.cust_name
ORDER BY o_c DESC;

-- What is the total and average purchase value per customer?
select * from order_details;
SELECT 
    c.cust_name,
    SUM(od.total_price) AS Total_purchase_value,
    round(AVG(od.total_price),2) AS avg_price
FROM
    customers c
        JOIN
    orders o ON c.cust_id = o.cust_id
        JOIN
    order_details od ON o.ord_id = od.ord_id
GROUP BY c.cust_name;
 
-- Who are the top 5 customers by total purchase amount?
SELECT c.cust_id,c.cust_name,
       SUM(od.total_price) AS Total_purchase_value
FROM customers c
JOIN orders o ON c.cust_id = o.cust_id
JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY c.cust_name
ORDER BY Total_purchase_value DESC
LIMIT 5;
-- 2. Product Performance
-- How many products exist in each category?
SELECT 
    c.cat_name, COUNT(p.cat_id) as num_products
FROM
    categories c
        JOIN
    products p ON c.cat_id = p.cat_id
GROUP BY c.cat_name;

-- What is the average price of products by category?
SELECT 
    c.cat_name, round(AVG(price),2) AS avg_price_products
FROM
    categories c
        JOIN
    products p ON c.cat_id = p.cat_id
GROUP BY c.cat_name;

-- Which products have the highest total sales volume (by quantity)?
SELECT 
    p.prod_name, SUM(od.quantity) AS Volume
FROM
    products p
        JOIN
    order_details od ON p.prod_id = od.prod_id
GROUP BY p.prod_name;

-- What is the total revenue generated by each product?
select * from order_details;
SELECT 
    p.prod_name, SUM(od.total_price) revenue_by_products
FROM
    products p
        JOIN
    order_details od ON p.prod_id = od.prod_id
GROUP BY prod_name;

-- How do product sales vary by category and supplier?
SELECT
    c.cat_id,
    c.cat_name,
    SUM(od.quantity) AS total_quantity_sold,
    SUM(od.total_price) AS total_sales
FROM categories c
JOIN products p
    ON c.cat_id = p.cat_id
JOIN order_details od
    ON p.prod_id = od.prod_id
GROUP BY c.cat_id, c.cat_name
ORDER BY total_sales DESC;

-- 3. Sales and Order Trends
-- How many orders have been placed in total?
SELECT 
    COUNT(ord_id) AS total_orders
FROM
    orders;

-- What is the average value per order?

SELECT 
    o.ord_id, round(AVG(od.total_price),2) AS avg_price
FROM
    orders o
        JOIN
    order_details AS od ON o.ord_id = od.ord_id
GROUP BY od.ord_id;

-- On which dates were the most orders placed?
SELECT 
    order_date, COUNT(ord_id) AS num_orders
FROM
    orders
GROUP BY order_date
order by num_orders desc;

-- What are the monthly trends in order volume and revenue?

SELECT 
    SUBSTR(o.order_date, 1, 1) AS month,
    COUNT(o.ord_id) AS count_of_orders,
    SUM(od.total_price) AS Revenue
FROM
    orders o
        JOIN
    order_details od ON o.ord_id = od.ord_id
GROUP BY month
ORDER BY Revenue DESC
;

-- How do order patterns vary across weekdays and weekends?
SELECT
    CASE
        WHEN DAYOFWEEK(order_date) IN (1,7)
        THEN 'Weekend'
        ELSE 'Weekday'
    END AS day_type,
    COUNT(*) AS total_orders
FROM orders
GROUP BY day_type;

-- 4️. Supplier Contribution
-- How many suppliers are there in the database?
SELECT 
    COUNT(sup_id)
FROM
    supplier;

-- Which supplier provides the most products?
SELECT 
    s.sup_name, COUNT(p.sup_id) AS most_provider
FROM
    supplier s
        JOIN
    products p ON s.sup_id = p.sup_id
GROUP BY s.sup_id
